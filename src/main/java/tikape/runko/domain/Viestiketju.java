package tikape.runko.domain;

public class Viestiketju {

    private int maara;
    private String nimi;

    public Viestiketju(int maara, String nimi) {
        this.maara = maara;
        this.nimi = nimi;
    }

    public int getMaara() {
        return maara;
    }

    public String getNimi() {
        return nimi;
    }
    

    

}
